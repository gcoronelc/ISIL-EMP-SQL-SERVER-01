SELECT A.CUR_NOMBRE AS CURSO, SUM(B.pag_importe) AS 'IMPORTE DE PAGO'  FROM EDUCA..CURSO A INNER JOIN EDUCA..PAGO B ON A.cur_id =B.cur_id
GROUP BY A.CUR_NOMBRE