SELECT A.alu_nombre, SUM(B.pag_importe) AS 'IMPORTE DE PAGO'  FROM EDUCA..ALUMNO A INNER JOIN EDUCA..PAGO B ON A.alu_id =B.alu_id
GROUP BY A.alu_nombre

